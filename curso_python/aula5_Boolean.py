# Tipo de dado bool(boolean)
# ao questionar algo em um programa, só existem duas respostas possíveis: true ou false
# Existem vários operadores para questionar: == (se um valor é igual ao outro)
#

print(10 == 10) # true
print(10 == 9) # false
print(type(10 == 10)) # informa o tipo de dado, no caso, boolean
