"""
Python = Linguagem de programação
Tipo de tipagem = Dinânimca / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas: pode ser aspas simples ou duplas
"""

print('Luis Otávio')
print("Luis Otávio")

# caracter de escape \ para mostrar as aspas em uma string
print("Luis \"Otávio\"")

# mostrar as barras junto com as aspas, incluindo a letra r no início do argumento
print(r"Luis \"Otávio\"")

# mostrando as aspas sem usar caracter de escape: aspas simples dentro de aspas duplas (e vice-versa)
print('Luis "Otávio"')
print("Luis 'Otávio'")