# Tipos int e float
# int -> número inteiro
# o tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11)
print(-11)
print(0)

# float -> números com ponto flutuante
# o tipo float representa qualquer número positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo
print(11.11, 10.11)
print(0.0, -1.5)

# a função type(que na verdade é uma classe) mostra o tipo que o Python inferiu ao valor
print(type('Otávio'))
print(type(123))
print(type(1.1), type(-1.1), type(0.0))